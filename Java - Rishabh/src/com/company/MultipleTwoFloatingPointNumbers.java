public class MultipleTwoFloatingPointNumbers {
    public static void main(String[] args) {
        float f1 = 41.66f,f2=32.3f;
        //the f after each indicates the numbers are of the float type
        float product;

        System.out.println("First floating number = "+f1);
        System.out.println("Second floating number = "+f2);
        product = f1*f2;

        System.out.println("Product = "+product);
    }
}

